#include <bits/stdc++.h>
using namespace std;

int main() {
  cout << 1 + 1 << endl;  // 2
  cout << 3 - 4 << endl;  // -1
  cout << 2 * 3 << endl;  // 6
  cout << 7 / 3 << endl;  // 2

  cout << 7 % 3 << endl;  // 1
  cout << 4 % 5 << endl;  // 4

  cout << (1 + 2) * 3 << endl;  // 9
  // 多くの場合、割り算はできるだけ後の方で行うようにしたほうが正しい結果になります。(切り捨ての関係上)
}
